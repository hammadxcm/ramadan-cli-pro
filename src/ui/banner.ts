const tagLine = "Sehar \u2022 Iftar \u2022 Ramadan timings";

/**
 * Returns the ASCII art banner with color, sized to the current terminal width.
 *
 * @returns The formatted banner string.
 */
export const getBanner = (): string => {
	const width = process.stdout.columns ?? 80;
	const art = width >= 120 ? ANSI_SHADOW : ANSI_COMPACT;
	const artColor = ramadanGreen(art.trimEnd());
	const lead = ramadanGreen(`  ${MOON_EMOJI} Ramadan CLI Pro`);
	const tag = pc.dim(`  ${tagLine}`);
	return `\n${artColor}\n${lead}\n${tag}\n`;
};
